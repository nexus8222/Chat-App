void display_emoji_tab_with_index() {
    printf("\n\033[1;36m[Emoji Panel]\033[0m\n");
    for (int i = 0; i < EMOJI_COUNT; i++) {
        printf("%2d. %s\t", i + 1, emoji_list[i]);
        if ((i + 1) % 6 == 0) printf("\n");
    }
    printf("\nType /emoji <number> to insert.\n");
}
    

const char* get_emoji_by_index(int idx) {
    if (idx < 1 || idx > EMOJI_COUNT) return NULL;
    return emoji_list[idx - 1];
}
